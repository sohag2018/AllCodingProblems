
public class NestedLoop {
//inner loop can be same as outer loop or different
	public static void main(String[] args) {
		System.out.println("X the correct ans.--------------");
		for(int i=1;i<=10;i++) {
			System.out.print(i+". ");
			for(int j=0;j<4;j++) {
				if(j==0) {
					System.out.print(" (a)");
				}
				if(j==1) {
					System.out.print(" (b)");
				}
				if(j==2) {
					System.out.print(" (c)");
				}
				if(j==3) {
					System.out.print(" (d)"+"\n");
				}
			}
		}
		
		
		
			
		}
		
		
		

	}


